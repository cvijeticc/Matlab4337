    private void populateTableTeachers() {
        List<Teacher> teachers;
        try {
            teachers = new Controller().getAllTeachers();
            TableModel tmt = new TeacherTableModel(teachers);
            tblTeachers.setModel(tmt);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

