    private void prepareTableTeachers() {
        try {
            //promeni default editor komponentu za kolonu u zvanje

            //kreiraj kombobox i popuni sa zvanjima
            List<AcademicTitle> titles = new Controller().getAllAcademicTitle();
            ComboBoxModel modelAcademicTitle = new DefaultComboBoxModel(titles.toArray());
            JComboBox comboTitles = new JComboBox(modelAcademicTitle);

            //postavi combobox da bude editor za kolonu zvanje
            TableColumn tc = tblTeachers.getColumnModel().getColumn(2);
            tc.setCellEditor(new DefaultCellEditor(comboTitles));

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

