    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        //vidi koji je red izabran
        int selectedRow = tblTeachers.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite red u tabeli!");
        } else {
            TeacherTableModel ttm = (TeacherTableModel) tblTeachers.getModel();
            ttm.remove(selectedRow);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        //sve iz tabele tblTeacher, sacuvaj u tabeli u bazi nastavnik

        //uzmi sve nastavnike iz tabele
        TeacherTableModel tm = (TeacherTableModel) tblTeachers.getModel();
        List<Teacher> teachers = tm.getTeachers();
        try {
            //ove nastavnike sacuvaj ili dodaj u tabelu
            new Controller().update(teachers, tm.getRemovedTeachers());
            JOptionPane.showMessageDialog(this, "Uspesno azuriranje!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

