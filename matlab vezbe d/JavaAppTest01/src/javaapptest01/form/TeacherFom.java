    private void populateComboAcademicTitles() {
        comboAcademicTitle.removeAllItems();
        try {
            //popini combo
            List<AcademicTitle> titles = new Controller().getAllAcademicTitle();
            comboAcademicTitle.setModel(new DefaultComboBoxModel(titles.toArray()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void prepareForm() {
        switch (mode) {
            case MyUIConstants.ADD_TEACHER: {
                txtId.setEnabled(false);
                btnDelete.setEnabled(false);
                btnEnableChanges.setEnabled(false);
                btnSave.setEnabled(true);
                comboAcademicTitle.setEditable(true);
                break;
            }
            case MyUIConstants.EDIT_TEACHER: {
                txtId.setEnabled(false);
                txtFirstname.setEnabled(true);
                txtLastname.setEnabled(true);
                comboAcademicTitle.setEditable(true);

                btnDelete.setEnabled(false);
                btnEnableChanges.setEnabled(false);
                btnSave.setEnabled(true);
                break;
            }
            case MyUIConstants.VIEW_TEACHER: {
                txtId.setEnabled(false);
                txtFirstname.setEnabled(false);
                txtLastname.setEnabled(false);
                comboAcademicTitle.setEditable(false);

                btnDelete.setEnabled(true);
                btnEnableChanges.setEnabled(true);
                btnSave.setEnabled(false);

                txtId.setText(teacher.getId() + "");
                txtFirstname.setText(teacher.getFirstname());
                txtLastname.setText(teacher.getLastname());
                comboAcademicTitle.setSelectedItem(teacher.getAcademicTitle());
                break;
            }
        }
    }
