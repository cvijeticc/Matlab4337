    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String firstname = txtFirstname.getText().trim();
        String lastname = txtLastname.getText().trim();
        AcademicTitle academicTitle
                = (AcademicTitle) comboAcademicTitle.getSelectedItem();

        if (this.mode == MyUIConstants.ADD_TEACHER) {
            //izvrsi validaciju
            if (!firstname.isEmpty() && !lastname.isEmpty()) {
                Teacher newTeacher = new Teacher();
                newTeacher.setFirstname(firstname);
                newTeacher.setLastname(lastname);
                newTeacher.setAcademicTitle(academicTitle);

                int option = JOptionPane.showConfirmDialog(this, "Da li zelite da sacuvate nastavnika?", "Cuvanje entiteta", JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.YES_OPTION) {
                    try {
                        new Controller().saveTeacher(newTeacher);
                        JOptionPane.showMessageDialog(this, "Nastavnik sacuvan!");
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, ex.getMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Odustajem");
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nisu uneti svi podaci!");
            }
        }else if (this.mode== MyUIConstants.EDIT_TEACHER){
            JOptionPane.showMessageDialog(this, "Izvrsi update!");
        }


    }//GEN-LAST:event_btnSaveActionPerformed


